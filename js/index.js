const memoryGame = new MemoryGame(cards);

window.addEventListener('load', event => {
  let html = '';
  memoryGame.cards.forEach(pic => {
    html += `<div class="card" data-card-name="${pic.name}">`;
    html += `<div class="back" name="${pic.img}"></div>`;
    html += `<div class="front" style="background: url(img/${pic.img}) no-repeat"></div>`;
    html += `</div>`;
  });

  // Add all the divs to the HTML
  document.querySelector('#memory-board').innerHTML = html;

  // Bind the click event of each element to a function
  document.querySelectorAll('.card').forEach(card => {
    card.addEventListener('click', () => {
      memoryGame.pickedCards.push(card.getAttribute("data-card-name"))

      card.classList.toggle("turned")
      if(memoryGame.pickedCards.length === 2) {
        let pair = memoryGame.checkIfPair(memoryGame.pickedCards[1], memoryGame.pickedCards[0])
        if(pair) {
          memoryGame.pickedCards = []
        } else {
            setTimeout(() => {
              document.querySelector(`[data-card-name = '${memoryGame.pickedCards[0]}']`).classList.remove("turned")
              document.querySelector(`[data-card-name = '${memoryGame.pickedCards[1]}']`).classList.remove("turned")
              memoryGame.pickedCards = []
            }, 2000)
          }
      }

      document.querySelector("#pairs-clicked").innerHTML = memoryGame.pairsClicked
      document.querySelector("#pairs-guessed").innerHTML = memoryGame.pairsGuessed

      memoryGame.isFinished()
    })
  })
})